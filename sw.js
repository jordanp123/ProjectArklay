// SCME service worker — offline support + safe auto-update.
//
// Strategy: an ATOMIC, versioned precache. On install we download the whole
// app shell into a cache named for CACHE_VERSION; we only ever serve files
// from that one version, so index.html and the css/js it pins by SRI can never
// be mismatched (a stale index.html + fresh app.js would fail the integrity
// check). A new deploy changes CACHE_VERSION, the browser installs the new
// worker in the background, and js/pwa.js offers the user a "Reload" toast —
// nothing swaps out from under an in-progress circuit until they choose.
//
// CACHE_VERSION and PRECACHE below are GENERATED by tools/gen-csp.py from the
// real asset bytes, so any changed file bumps the version automatically. Run
// `python3 tools/gen-csp.py` after editing assets. Do not hand-edit the block.

