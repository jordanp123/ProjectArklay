// ── Install: fill the versioned cache, then wait. ───────────────────────
// No skipWaiting() here: the fresh worker stays in "waiting" until the user
// accepts the update (pwa.js posts SKIP_WAITING), so we never reload mid-edit.
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_VERSION).then((cache) => cache.addAll(PRECACHE))
  );
});

// ── Activate: drop old versions, take control. ──────────────────────────
self.addEventListener('activate', (event) => {
  event.waitUntil((async () => {
    const keys = await caches.keys();
    await Promise.all(
      keys.filter((k) => k.startsWith('scme-') && k !== CACHE_VERSION)
          .map((k) => caches.delete(k))
    );
    await self.clients.claim();
  })());
});

// ── Fetch: cache-first from the versioned set; network only for misses. ──
// Everything served in a page load comes from one CACHE_VERSION, so the SRI
// pins in index.html always match. Offline navigations fall back to the shell.
self.addEventListener('fetch', (event) => {
  const req = event.request;
  if (req.method !== 'GET') return;                       // GET only
  const url = new URL(req.url);
  if (url.origin !== self.location.origin) return;        // same-origin only
  event.respondWith(handle(req));
});

async function handle(req) {
  const cache = await caches.open(CACHE_VERSION);
  const cached = await cache.match(req, { ignoreSearch: true });
  if (cached) return cached;
  try {
    return await fetch(req);
  } catch (err) {
    if (req.mode === 'navigate') {
      const shell = (await cache.match('index.html')) || (await cache.match('./'));
      if (shell) return shell;
    }
    throw err;
  }
}

// ── The waiting worker activates as soon as the user accepts the update. ─
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') self.skipWaiting();
});
